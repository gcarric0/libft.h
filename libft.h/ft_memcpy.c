#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*d;
	unsigned char	*s;

	i = 0;
	d = (char *)dest;
	s = (char *)src;
	while (i++ < n)
		*d++ = *s++;
	return (dest);
}
